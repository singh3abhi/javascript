// For loop

for (let i = 0; i <= 100; i++) {
  console.log(i);
}

// Reverse For Loop

for (let i = 100; i >= 0; i--) {
  console.log(i);
}

// While Loop
let i = 0;
while (i <= 100) {
  console.log(i);
  i++;
}

// Reverse While Loop
i = 100;
while (i >= 0) {
  console.log(i);
  i--;
}

// Do While

i = 0;
do {
  console.log(i);
  i--;
} while (i > 0);
